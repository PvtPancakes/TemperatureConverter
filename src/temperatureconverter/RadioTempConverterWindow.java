    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        double originalTemp = 0;
        double calculatedTemp = 0;
        
        try{
            originalTemp = Double.parseDouble(txtInput.getText());
            if(this.toCelsiusRadio.isSelected()){
                calculatedTemp = (originalTemp - 32) * (5.0 / 9);
            }else{
                calculatedTemp = (originalTemp * (9.0 / 5)) + 32;
            }
            lblOutput.setText("" + df.format(calculatedTemp) + "Â°C");
            errorOutput.setText("");
        }catch(NumberFormatException e){
            lblOutput.setText("xxx");
            errorOutput.setText("Please enter a valid number.");
            return;
        }     
        
    }//GEN-LAST:event_jButton1ActionPerformed

